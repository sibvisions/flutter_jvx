abstract class MaterialIconUtil {
  static const String MATERIAL_PREFIX = "Material";

  static bool checkMaterial(String pText) {
    return pText.contains(MATERIAL_PREFIX);
  }

  static Icon getMaterialIcon({required String pText, double? pIconSize, Color? pColor}) {
    List<String> arr = pText.split(",");

    String iconName = arr[0];
    double? iconSize;

    if (iconName.contains(";")) {
      var nameAndSize = iconName.split(";");

      // Currently ignore "size=X" parameter;
      iconName = nameAndSize[0];
    }

    if (arr.length >= 2) {
      iconSize = double.parse(arr[1]);
    }

    Color? iconColor;

    if (arr.length > 4) {
      iconColor = ParseUtil.parseHexColor(arr[4]);
    }

    if (pColor != null) {
      iconColor = pColor;
    }

    if (pIconSize != null) {
      iconSize = pIconSize;
    }

    IconData iconData = ICONS[iconName] ?? Icons.question_mark;

    return Icon(
      iconData,
      size: iconSize,
      color: iconColor,
    );
  }

